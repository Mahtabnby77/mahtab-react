function sum(num1 , num2) {
    return num1 + num2
}

function sub(num1 , num2) {
    return num1 - num2
}

function mul(num1 , num2) {
    return num1 * num2
}

function div(num1 , num2) {
    return num1 / num2
}

 console.log(`5 + 12 = ${sum(5 , 12)}`);
 console.log(`12 - 6 = ${sub(12 , 6)}`);
 console.log(`7 * 3 = ${mul(7 , 3)}`);
 console.log(`15 / 3 = ${div(15 , 3)}`);